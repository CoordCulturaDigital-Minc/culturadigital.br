<p><strong>Simple href links:</strong> AMP will play any href link to MP3, Flash Video (flv), Quicktime and Windows Media files.</p>
<p>Upload your <em>mp3, flv, Quicktime mov, mp4, m4v, m4a, m4b, 3gp, Windows Media wmv, avi or asf</em> file via the WordPress editor's upload browser then "send to editor" - or make a hypertext link to any external file on the web - and you're done!</p>
<p>For Quicktime movs you can replace the default AMP placeholder image by uploading a jpeg to the same directory as your movie (e.g., my-movie.mov) and giving it the filename <em>my-movie.mov.jpg</em>. AMP will then load your custom image when playing that movie.</p>
<p><strong>WordPress Editor:</strong> For custom Flash swf embeds use the Flash "&fnof;" button, likewise use the Director "d" button for embedding dcr movies. For all other supported media player formats (including Google Video, YouTube, Apple iMixes etc.,) as well as full url's to href linked media (mp3, flv, mov, wmv etc.) use the "A" for Anarchy button.</p>

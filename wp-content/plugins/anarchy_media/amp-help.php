<p><strong>Disable mp3 links:</strong> To stop AMP capturing a media link put <em>class="amplink"</em> inside the <em>a href</em>. This works for simple url's to mp3, mp4, mov, flv, asf, avi and wmv files.</p>
<p><strong>Web devs:</strong> The SWF embeds are handled by Kmil Flash Embed and its Javascript handler SWFObject. The <a target="_blank" href="http://www.kimili.com/plugins/kml_flashembed">Kmil tags</a> in WP, and <a target="_blank" href="http://blog.deconcept.com/flashobject/">SWFObject javascript</a> in html provide a very flexible framework for embedding swf files including multiple fvars. SWFObject is also included in the standalone javascript. If you are running AMP in a WordPress install simply copy the script embed from a WP page header and paste that into non-WP sites, for example a BBPress forum or MediaWiki, to make use of both simple href media links as well as SWFObject Flash embeds.</p>
